/**
 * @desc Session kullanımı
 * @param $par
 * @return bool|mixed
 */
function session($par){
    if ( $_SESSION[$par] ){
        return $_SESSION[$par];
    }else{
        return false;
    }
}


/**
 * @desc Session oluşturma
 * @param $par
 */
function sessionCreate($par){
    foreach ($par as $anahtar => $deger){
        $_SESSION[$anahtar] = $deger;
    }
}


/**
 * @desc Cookie kullanımı
 * @param $par
 * @return bool|mixed
 */
function cookie($par){
    if ($_COOKIE[$par]){
        return $_COOKIE[$par];
    }else {
        return false;
    }
}


/**
 * @desc stripslashes ile \ işaretlerini kaldırma
 * @param $par
 * @return string
 */
function ss($par){
    return stripslashes($par);
}


/**
 * @desc $_Post metodu
 * @param $par
 * @param bool $st
 * @return string
 */
function p($par, $st = false){
    if ($st){
        return htmlspecialchars(addslashes(trim($_POST[$par])));
    }else {
        return addslashes(trim($_POST[$par]));
    }
}


/**
 * @desc $_GET metodu
 * @param $par
 * @param bool $st
 * @return string
 */
function g($par){
    return trim(strip_tags(addslashes($_GET[$par])));
}

