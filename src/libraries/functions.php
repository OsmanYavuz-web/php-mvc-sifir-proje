/**
 * @desc Dil sistemi için dizeye aktarılan veriyi çekme
 * @param $lgParam
 * @return mixed
 */
function __($lgParam){
    global $lgTranslate;
    if(isset($lgTranslate[$lgParam])){
        return $lgTranslate[$lgParam];
    }
    return $lgParam;
}


/**
 * @desc Sitenin ana url adresi
 * @return string
 */
function BaseUrl(){
    if(isset($_SERVER['HTTPS'])){
        $protocol = ($_SERVER['HTTPS'] && $_SERVER['HTTPS'] != "off") ? "https" : "http";
    } else {
        $protocol = 'http';
    }
    return $protocol . "://" . $_SERVER['HTTP_HOST'] . $_SERVER['REQUEST_URI'];
}


